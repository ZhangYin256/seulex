static int accept_action[6] = { -1, 0, 1, 2, 3, 4 };

int yylex(void) {
  if (!seulex_buf) {
    size_t cap = 4096; seulex_buf = (char *)malloc(cap); if (!seulex_buf) return 0;
    int ch; while ((ch = fgetc(stdin)) != EOF) { if (seulex_n + 1 >= cap) { cap *= 2; char *tmp = (char *)realloc(seulex_buf, cap); if (!tmp) { free(seulex_buf); seulex_buf = NULL; return 0; } seulex_buf = tmp; } seulex_buf[seulex_n++] = (char)ch; }
    if (!seulex_buf) return 0;
  }
  while (seulex_pos < seulex_n) {
    int state = 0;
    size_t cur = seulex_pos; int last_accept = -1; size_t last_pos = seulex_pos;
    while (cur < seulex_n) { unsigned char ch = (unsigned char)seulex_buf[cur]; int next = transition_table[state][ch]; if (next == -1) break; state = next; cur++; if (accept_action[state] != -1) { last_accept = accept_action[state]; last_pos = cur; } }
    if (last_accept == -1) { seulex_pos++; continue; }
    size_t matched_len = last_pos - seulex_pos;
    size_t copy_len = matched_len < (SEULEX_YYTEXT_MAX - 1) ? matched_len : (SEULEX_YYTEXT_MAX - 1);
    memcpy(yytext, seulex_buf + seulex_pos, copy_len); yytext[copy_len] = '\0'; yyleng = (int)copy_len;
    // Execute action for matched token
    switch (last_accept) {
    case 0: {
 char_count++; 
      break;
    }
    case 1: {
 digit_count++; 
      break;
    }
    case 2: {
 space_count++; 
      break;
    }
    case 3: {
 line_count++; 
      break;
    }
    case 4: {
 /* 忽略其他字符 */ 
      break;
    }
    default: break;
    }
    seulex_pos = last_pos;
  }
  return 0;
}
